package relationOperations;

public class relationOperations {
    public static void main(String[] args) {


        int x = 6;
        int y = 5;

        boolean result = x == y;
        boolean result2 = x <= y;
        boolean result3 = x >= y;
        boolean result4 = x != y;

        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);

    }
}
